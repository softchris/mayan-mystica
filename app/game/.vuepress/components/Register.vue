<template>
  <div class="w-full max-w-xs">
    <h1>{{ $t('register') }}</h1>
    <form class="bg-white px-8 pt-6 pb-8 mb-4" @submit.prevent="submit">
      <p class="text-red-500 text-xs italic" aria-live="polite">{{ message }}</p>

      <div class="mb-4">
        <label class="mb-2" for="email">
          Email
          <span class="text-red-500">*</span>
        </label>
        <input
          class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
          id="email"
          type="email"
          v-model="email"
          placeholder="Email"
          required
          tabindex="0"
        />
      </div>
      <div class="mb-6">
        <label class="mb-2" for="password">
          Password
          <span class="text-red-500">*</span>
        </label>
        <input
          class="shadow appearance-none border border-red-500 rounded w-full py-2 px-3 text-gray-700 mb-3 leading-tight focus:outline-none focus:shadow-outline"
          id="password"
          type="password"
          placeholder="Password"
          v-model="password"
          required
        />
      </div>
      <div class="flex justify-between">
        <button
          class="bg-transparent font-semibold hover:text-white py-2 px-4 border border-blue-500 hover:border-transparent rounded"
          type="submit"
        >{{ $t('register') }}</button>
      </div>
    </form>

    <router-link class="has-text-grey" to="../login">
      <p>{{ $t('login') }}</p>
    </router-link>
  </div>
</template>
<script>
import axios from "axios";
import messages from "@theme/translations/misc.js";
import { EventBus } from "@theme/utils/event-bus";

import {
  getUID,
  setUID,
  hasUID,
  setSessionTicket,
  getSessionTicket,
  getLocale
} from "@theme/utils/helpers";
export default {
  data() {
    return {
      message: "",
      email: "",
      password: ""
    };
  },
  methods: {
    submit() {
      this.message = "";
      axios
        .post("https://maya-mystery-api.azurewebsites.net/api/register", {
          email: this.email,
          password: this.password
        })
        .then(response => {
          console.log(response);
          if (response.data.errorMessage == null) {
            setSessionTicket(response.data.SessionTicket);
            this.linkCustomID();
            this.message = this.$t("registerwelcome");
          } else {
            this.message = response.data.errorMessage;
          }
        })
        .catch(function(error) {
          console.log(error);
        });
    },
    linkCustomID() {
      axios
        .post("https://maya-mystery-api.azurewebsites.net/api/linkCustomId", {
          id: getUID(),
          session: getSessionTicket()
        })
        .then(response => {
          console.log(response);
          //todo, where should the user go at this point?
        })
        .catch(function(error) {
          console.log(error);
        });
    }
  },
  created() {
    this.$i18n.locale = getLocale();
    EventBus.$on("lang_changed", lang => (this.$i18n.locale = lang));
  },
  beforeDestroy() {
    EventBus.$off("lang_changed");
  }
};
</script>
